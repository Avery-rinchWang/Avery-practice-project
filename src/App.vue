// ---------- 表单相关 ----------
const username = ref('')
const gender = ref<string | null>(null)
const agree = ref(false)

const usernameActive = ref(false)
const radioActive = ref(false)
const agreeActive = ref(false)

const showOverlay = ref(false)

const formRef = ref<HTMLFormElement | null>(null)
const cardRef = ref<HTMLElement | null>(null)

// 清除所有 active 标记
function clearActive(): void {
  usernameActive.value = false
  radioActive.value = false
  agreeActive.value = false
}

// 校验并标记未填项，返回是否全部合格
function validateAndMark(): boolean {
  clearActive()
  let isComplete = true

  if (username.value.trim() === '') {
    usernameActive.value = true
    isComplete = false
  }

  if (gender.value === null || gender.value === '') {
    radioActive.value = true
    isComplete = false
  }

  if (!agree.value) {
    agreeActive.value = true
    isComplete = false
  }

  return isComplete
}

// 表单输入/选择事件：移除对应的 active 状态
function onUsernameInput(): void {
  if (username.value.trim() !== '') {
    usernameActive.value = false
  }
}
function onGenderChange(): void {
  radioActive.value = false
}
function onAgreeChange(): void {
  if (agree.value) {
    agreeActive.value = false
  }
}

// 点击确认按钮
function handleConfirm(): void {
  if (validateAndMark()) {
    showOverlay.value = true
  }
}

// 关闭弹窗
function closeOverlay(): void {
  showOverlay.value = false
}

// 点击表单外部移除所有 active
function handleClickOutside(event: MouseEvent): void {
  if (formRef.value && !formRef.value.contains(event.target as Node)) {
    clearActive()
  }
}

onMounted(() => {
  document.addEventListener('click', handleClickOutside)
  // 原控制台输出 card 的 className，保留此行为
  if (cardRef.value) {
    console.log('className:', cardRef.value.className)
  }
})

onUnmounted(() => {
  document.removeEventListener('click', handleClickOutside)
})

// ---------- 待办清单相关 ----------
interface Todo {
  id: number
  text: string
  completed: boolean
}

let nextTodoId = 1
const todoList = ref<Todo[]>([])
const newTodoText = ref('')
const todoInputRef = ref<HTMLInputElement | null>(null)

const isEmpty = computed(() => todoList.value.length === 0)

function addTodo(text: string, completed = false): boolean {
  const cleanText = text.trim()
  if (cleanText === '') return false
  todoList.value.push({
    id: nextTodoId++,
    text: cleanText,
    completed,
  })
  return true
}

async function handleAddTodo(): Promise<void> {
  if (newTodoText.value.trim() === '') {
    // 输入为空时主动聚焦，显示蓝色边框
    todoInputRef.value?.focus()
    return
  }
  if (addTodo(newTodoText.value)) {
    newTodoText.value = ''
    await nextTick()
    todoInputRef.value?.focus()
  }
}

function onTodoKeyPress(event: KeyboardEvent): void {
  if (event.key === 'Enter') {
    event.preventDefault()
    handleAddTodo()
  }
}

function deleteTodo(id: number): void {
  todoList.value = todoList.value.filter((todo) => todo.id !== id)
}

function toggleTodo(id: number): void {
  const target = todoList.value.find((todo) => todo.id === id)
  if (target) {
    target.completed = !target.completed
  }
}

// 初始数据
function initSampleTodos(): void {
  const samples = [
