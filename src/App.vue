    },
    {
      text: '一起玩游戏，可能需要一点点好脾气，因为Avery很菜（）',
      completed: false,
    },
    { text: '一起出去玩，只是散散步也可以', completed: false },
    {
      text: '约饭约饭，一起吃好吃的是件顶顶好的事情',
      completed: false,
    },
  ]
  samples.forEach((s) => addTodo(s.text, s.completed))
}

initSampleTodos()
</script>

